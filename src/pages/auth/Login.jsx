import { useContext, useRef, useState } from "react";
import { Link, useNavigate } from "react-router";
import { toast } from "react-toastify";
import { motion } from "motion/react";
import { AuthContext } from "../../context/AuthContext";

export default function Login() {
    const [error, setError] = useState('');
    const { loginUser, continueWithGoogle } = useContext(AuthContext);
    const emailRef = useRef();
    const navigate = useNavigate();
    function handleLogin(e) {
        e.preventDefault();
        const email = e.target.email.value.trim();
        const password = e.target.password.value.trim();
        setError('');
        const regexUpLow = /^(?=.*[a-z])(?=.*[A-Z]).+$/;
        if (password.length < 6) {
            setError('Password should be atleast 6 characters!');
            return;
        } else if (!regexUpLow.test(password)) {
            setError('Atleast one uppercase and lowercase required');
            return;
        }
        loginUser(email, password)
            .then(userCredintial => {
                e.target.reset();
                toast.success(`Welcome ${userCredintial.user.displayName}`);
                navigate('/');
            })
            .catch(error => setError(error.message));
    }
    const googleAuth = () => {
        continueWithGoogle()
            .then(result => {
                toast.success(`Welcome ${result.user.displayName}`);
                navigate('/');
            })
            .catch(error => setError(error.message));
    }
    return (
        <section className="hero min-h-[80vh]">
            <title>Login</title>
            <div className="hero-content w-full">
                <motion.div
                    className="card bg-base-100 w-full max-w-sm shadow-2xl"
                    initial={{ opacity: 0, y: -20 }}
                    animate={{ opacity: 1, y: 0 }}
                    transition={{ duration: 0.5 }}
                >
                    <div className="card-body rounded-2xl">
                        <h1 className="text-4xl font-semibold my-3 text-center">Welcome Back!</h1>
                        <form onSubmit={handleLogin}>
                            <fieldset className="fieldset">
                                <label className="label">Email</label>
                                <input type="email" ref={emailRef} className="input w-full placeholder:opacity-50" placeholder="Email" name='email' required />
                                <label className="label">Password</label>
                                <input type="password" className="input w-full placeholder:opacity-50" placeholder="Password" name='password' required />
                                <div><button onClick={() => navigate('/auth/forgot-password', { state: { email: emailRef.current.value } })} className="link link-hover">Forgot password?</button></div>
                                {
                                    error && <p className="text-base text-red-400 ">{error}</p>
                                }
                                <button className="btn btn-primary mt-4">Login</button>
                            </fieldset>
                        </form>
                        <p className="text-center text-lg">Or</p>
                        {/* Google */}
                        <button onClick={googleAuth} className="btn bg-white text-black border-[#e5e5e5]">
                            <svg aria-label="Google logo" width="16" height="16" xmlns="http://www.w3.org/2000/svg" viewBox="0 0 512 512"><g><path d="m0 0H512V512H0" fill="#fff"></path><path fill="#34a853" d="M153 292c30 82 118 95 171 60h62v48A192 192 0 0190 341"></path><path fill="#4285f4" d="m386 400a140 175 0 0053-179H260v74h102q-7 37-38 57"></path><path fill="#fbbc02" d="m90 341a208 200 0 010-171l63 49q-12 37 0 73"></path><path fill="#ea4335" d="m153 219c22-69 116-109 179-50l55-54c-78-75-230-72-297 55"></path></g></svg>
                            Login with Google
                        </button>
                        <p className="text-sm mt-2">New Here ? <Link to='/register' className="underline">Register</Link></p>
                    </div>
                </motion.div>
            </div>
        </section>
    );
}